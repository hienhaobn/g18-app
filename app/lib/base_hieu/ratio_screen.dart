extension GetSizeScreen on num {
  double get w {
    return (ratioWidth ?? 1) * this;
  }

  double get h {
    return (ratioHeight ?? 1) * this;
  }

  double get sp {
    return this * scaleFontsize;
  }
}

extension ShowString on String {
  String get show {
    if (this.isEmpty) {
      return '';
    }
    return this;
  }
}

